package fr.revoicechat.opengraph;

public final class OpenGraphSchema {

  OpenGraphBasicData basic;
  OpenGraphImage image;
  OpenGraphPage page;
  OpenGraphVideo video;
  OpenGraphAudio audio;
  OpenGraphArticle article;
  OpenGraphBook book;
  OpenGraphProfile profile;
  OpenGraphMusic music;

  public OpenGraphBasicData getBasic() {return basic;}

  public OpenGraphImage getImage() {return image;}

  public OpenGraphPage getPage() {return page;}

  public OpenGraphVideo getVideo() {return video;}

  public OpenGraphAudio getAudio() {return audio;}

  public OpenGraphArticle getArticle() {return article;}

  public OpenGraphBook getBook() {return book;}

  public OpenGraphProfile getProfile() {return profile;}

  public OpenGraphMusic getMusic() {return music;}
}